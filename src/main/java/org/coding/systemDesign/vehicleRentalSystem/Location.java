package org.coding.systemDesign.vehicleRentalSystem;

public class Location {

    int pinCode;
    String city;
    String state;
    String country;

    public Location(int pinCode, String city, String state, String country) {
        this.pinCode = pinCode;
        this.city = city;
        this.state = state;
        this.country = country;
    }
}
